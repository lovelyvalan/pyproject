'''
The Python interface is a straightforward transliteration of the Unix system call and library interface for sockets
 to Python’s object-oriented style:

  the socket() function returns a socket object whose methods implement the various socket system calls.

 Parameter types are somewhat higher-level than in the C interface: as with read() and write() operations on Python files,
 buffer allocation on receive operations is automatic, and buffer length is implicit on send operation
'''

'''

The pickle module implements binary protocols for serializing and de-serializing a Python object structure.

 “Pickling” is the process whereby a Python object hierarchy is converted into a byte stream, and “unpickling” is the inverse operation,
  whereby a byte stream (from a binary file or bytes-like object) is converted back into an object hierarchy.
 Pickling (and unpickling) is alternatively known as “serialization”, “marshalling,” 1 or “flattening”;
 however, to avoid confusion, the terms used here are “pickling” and “unpickling”.
'''

